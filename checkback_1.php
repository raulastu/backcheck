<div class='sizzle-content clearfix'>
<div class='info'>
<p>AngelHack organizes hackathon competitions for web developers and entrepreneurs, called AngelHacks, where they can learn developer tools, meet new friends, and receive funding and mentorship for their startup ideas. In 2013, AngelHack will organize 100 hackathons, bringing together 15,000+ developers in 50+ cities. Winners from every city are put through the AngelHack Accelerator Program, where we mentor them for 12 weeks before bringing them out to Silicon Valley to raise investment and introduce them to incubators.</p>
<p>Previous winners have gone on to attend Y-Combinator, AngelPad, TechStars, 500 Startups, and incubators around the world.</p>
